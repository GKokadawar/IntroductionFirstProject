package Datatypes;

public class Datatype {

	public static void main(String[] args) {
		//Numeric data types
		int a =10,b=20,c=30;
		System.out.println("The value of total "+(a+b+c));
		byte by=10;
		System.out.println(by);
		short sh=32767;
		System.out.println(sh);
		long l=1000769889;
		System.out.println(l);
		//Decimal
		float fl=14.53f;
		System.out.println(fl);

		double db= 1233.4123412123123;
		System.out.println(db);

		char grade='A';
		System.out.println(grade);

		String name="Girish";
		System.out.println(name);

		boolean bl= true;
		System.out.println(bl);

	}

}
